// First code!

fn main() {

    // println! - prints with a newline like "hello\n"
    println!("Back to hello world after so many years! Welcome");

    // print!() - prints without a newline
    print!("Testing print!()");

    // String interpolation
    println!();
    println!("Name {}", "Marley");

    // Positional arguments
    println!("{1} {0}", "0", "1");

}